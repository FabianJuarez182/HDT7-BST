public class association<k,v> {
    k llave; //Declara un objeto de tipo k
    v valor; //Declara un objeto de tipo v

    association(){
        llave=null;
        valor = null;
    }
    //Pase al constructor una referencia a un objeto de tipo T.
    association(k key, v value){
        llave=key;
        valor = value;
    }
    k getK(){
        return llave;
    }
    v getV(){
        return valor;
    }

}
